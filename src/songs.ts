export function getPianoKeysForSongNotes(notes: string[]): PianoKeyType[] {
  return notes.map(noteName => {
    const matchedKey = PIANO_KEYS.find(k => k.note === noteName);
    if (!matchedKey) {
      return PIANO_KEYS[5]; // fallback to Middle C4 instead of G3
    }
    return matchedKey;
  });
}
